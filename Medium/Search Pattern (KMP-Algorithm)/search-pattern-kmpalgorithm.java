class Solution
{
    
    ArrayList<Integer> search(String pat, String txt)
    {
        ArrayList<Integer> ans = new ArrayList<>();
        // your code here
        int i = 0; 
        while(i>=0){
            i = txt.indexOf(pat,i);
            if(i==-1) break;
            ans.add(++i);
        }
        if(ans.isEmpty()) ans.add(-1);
        return ans;
    }
}