class Solution {
    // Function to rotate a linked list.
    public Node rotate(Node head, int k) {
        // add code here
        
        int size = Size(head);
        if(k == 0 || size <= k) return head;
        
        Node temp1 = head;
        Node temp2 = head;
        for(int i = 1; i<k; i++){
            temp1 = temp1.next;
        }
        
        head = temp1.next;
        temp1.next = null;
        
        Node temp3 = head;
        while(temp3.next != null){
            temp3 = temp3.next;
        }
        
        temp3.next = temp2;
        return head;
    }
    
    public int Size(Node head){
        Node temp = head;
        int size = 1;
        while(temp.next!=null){
            temp = temp.next;
            size++;
            
        }
        return size;
    }
}


